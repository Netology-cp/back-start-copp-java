public class CycleParams {
    private int work;
    private int breake;
    private int count;

    public int getWork() {
        return work;
    }

    public void setWork(int work) {
        this.work = work;
    }

    public int getBreake() {
        return breake;
    }

    public void setBreake(int breake) {
        this.breake = breake;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
